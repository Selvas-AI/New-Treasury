/** 현재 pathname으로 해당 도움말 콘텐츠를 찾습니다 */
export function findHelp(pathname: string): HelpContent | null {
  const exact = HELP_MAP[pathname]
  if (exact) return exact
  const match = Object.keys(HELP_MAP)
    .filter(k => pathname.startsWith(k))
    .sort((a, b) => b.length - a.length)[0]
  return match ? HELP_MAP[match] : null
}
